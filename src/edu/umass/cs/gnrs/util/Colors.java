    public static Color getColor(String name) {
	return getColor(name, false);
    }

    public static Color getColor(String name, boolean required) {
	return getColor(name, required, null);
    }
    
    public static  Color getColor(String name, Color defaultValue) {
	return getColor(name, false, defaultValue);
    }
    
    private static Color getColor(String name, boolean required, Color defaultValue) throws ColorParseException {
	Color result = defaultValue;
	try {
	    result = (Color) Colors.class.getField(name).get(null);
	} catch (Exception ex) {
	    if (required) {
		throw new ColorParseException(name);
	    }
	}
	return result;
    }

    static class ColorParseException extends RuntimeException
    {
	String name;

	public ColorParseException(String name) 
	{ 
	    this.name = name;
	} 

	public String getName() {
	    return this.name;
	}
    }

